<?php

namespace App\Classes;

use App\Classes\Parts\Vehicle;
use App\Interfaces\Builder;

class Director
{
    public function build(Builder $builder): Vehicle
    {
        $builder->createVehicle();
        $builder->addDoors();
        $builder->addEngine();
        $builder->addWheel();

        return $builder->getVehicle();
    }
}
